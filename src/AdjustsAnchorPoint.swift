import UIKit
import MaterialMotionRuntime

/**
 Adjusts the anchor point of a target UIView while maintaining the view's current visible frame each
 time a gesture recognizer starts.

 Supported target types: UIView.
 */
@objc(MDMAdjustsAnchorPoint)
public final class AdjustsAnchorPoint: NSObject, Plan {
  /** The gesture recognizer whose events will be observed. */
  public var gestureRecognizer: UIGestureRecognizer

  /** Events generated by the provided gesture recognizer will be observed. */
  public init(withGestureRecognizer gestureRecognizer: UIGestureRecognizer) {
    self.gestureRecognizer = gestureRecognizer
    super.init()
  }

  /** The performer that will fulfill this plan. */
  public func performerClass() -> AnyClass {
    return AnchorPointPerformer.self
  }

  /** Returns a copy of this plan. */
  public func copy(with zone: NSZone? = nil) -> Any {
    return AdjustsAnchorPoint(withGestureRecognizer: gestureRecognizer)
  }
}
